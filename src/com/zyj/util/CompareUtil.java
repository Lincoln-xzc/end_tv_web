package com.zyj.util;


public class CompareUtil {
	public static boolean compareIntGT(Object p1, Object p2)
    {
        return NumberUtil.parseInt(p1) > NumberUtil.parseInt(p2);
    }

    public static boolean compareIntGE(Object p1, Object p2)
    {
        return NumberUtil.parseInt(p1) >= NumberUtil.parseInt(p2);
    }

    public static boolean compareIntEQ(Object p1, Object p2)
    {
        return NumberUtil.parseInt(p1) == NumberUtil.parseInt(p2);
    }
}
